function test_smoothing()
        mat1 = [1.0, 2.0]
        mat2 = [
                1.0222393236943468,
                1.1016905849861969,
                1.1915263437469639,
                1.289583214110971,
                1.3933349449133303,
                1.5,
                1.6066650550866697,
                1.710416785889029,
                1.8084736562530361,
                1.8983094150138031,
                1.9777606763056532,
        ]
        @test isapprox(smoothing(mat1, step = 0.1), mat2)
        mat3 = [1.0 2.0; 3.0 4.0]
        mat4 = [
                1.04448 2.04448
                1.20338 2.20338
                1.38305 2.38305
                1.57917 2.57917
                1.78667 2.78667
                2.0 3.0
                2.21333 3.21333
                2.42083 3.42083
                2.61695 3.61695
                2.79662 3.79662
                2.95552 3.95552
        ]
        @test isapprox(smoothing(mat3, step = 0.1), mat4,atol=0.0001)

end
